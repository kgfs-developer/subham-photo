let currentFilter = "all";

function filterGallery(filter, event) {
  currentFilter = filter;
  renderGallery();

  // Toggle active class on buttons
  document.querySelectorAll(".filter-btn").forEach((btn) => {
    btn.classList.remove("active");
  });
  event.target.classList.add("active");
}

function renderGallery() {
  const galleryContainer = document.getElementById("galleryImages");
  galleryContainer.innerHTML = "";
  const images = galleryImages[currentFilter] || [];

  images.forEach((src) => {
    const imgDiv = document.createElement("div");
    imgDiv.className = "col-md-4 mb-4";
    imgDiv.innerHTML = `<img src="${src}" alt="Gallery Image" class="img-fluid rounded" loading="lazy">`;
    galleryContainer.appendChild(imgDiv);
  });
}

// Initialize gallery on page load
renderGallery();

// FAQ toggle
function toggleFAQ(element) {
  element.classList.toggle("active");
}

// Contact form submission

let lat = "",
  lang = "";

// Get user location
if (navigator.geolocation) {
  navigator.geolocation.getCurrentPosition(
    function (position) {
      lat = position.coords.latitude;
      lang = position.coords.longitude;
    },
    function (error) {
      console.warn("Location error:", error);
    }
  );
} else {
  alert("Geolocation is not supported by this browser.");
}

// Handle form submit
document.getElementById("contactForm").addEventListener("submit", function (e) {
  e.preventDefault();

  // Get form values
  const name = document.getElementById("name").value.trim();
  const email = document.getElementById("email").value.trim();
  const phone = document.getElementById("phone").value.trim();
  const eventDate = document.getElementById("eventDate").value.trim();
  const eventType = document.getElementById("eventType").value.trim();
  const venue = document.getElementById("venue").value.trim();
  const requirements = document.getElementById("requirements").value.trim();

  // Construct WhatsApp message
  const message = `*New Event Booking Request*
Full Name: ${name}
Email: ${email}
Phone: ${phone}
Event Date: ${eventDate}
Event Type: ${eventType}
Venue Address: ${venue}
Additional Requirements: ${requirements || "None"}
Location: https://www.google.com/maps/search/?api=1&query=${lat},${lang}`;

  window.open(`https://wa.me/919798107150?text=${encodeURIComponent(message)}`);

  this.reset();
});
